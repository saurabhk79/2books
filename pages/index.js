import Head from "next/head";
import Link from "next/link";

import React, { useState } from "react";
import { ClipLoader } from "react-spinners";
import randomWords from "random-words";

import BookCard from "../components/BookCard";
import styles from "../styles/Home.module.css";

export default function Home() {
  const [search, setSearch] = useState("");
  const [books, setBooks] = useState([]);
  const [randomWd, setRandomWd] = useState("");

  const [loading, setLoading] = useState(true);
  const key = "AIzaSyBEaOB_V5t09GVGU45QsIgjmFD3CeOSc5A";

  // setting search value randomly
  const getFunBooks = () => {
    let random = randomWords();
    setRandomWd(random);
    getBooks(random);
    setSearch("");
  };

  // fetching the books
  const getBooks = async (search) => {
    if (search.length) {
      setLoading(true);
      const res = await fetch(
        `https://www.googleapis.com/books/v1/volumes?q=${search}&max_results=18&printType=books&key=${key}`
      );
      const data = await res.json();
      setBooks(data.items);

      setSearch("");
      setTimeout(() => {
        setLoading(false);
      }, 2000);
    }
  };

  return (
    <>
      <Head>
        <title>2Books</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
      </Head>

      <main>
        <div className={styles.home}>
          <div className={styles.titleName}>
            2Bo<span className={styles.colText}>oks</span>
          </div>

          <input
            type="text"
            className={styles.searchBar}
            value={search}
            onChange={(e) => setSearch(e.target.value)}
            placeholder="Give me a book..."
          />
          <div className={styles.btnGroup}>
            <button
              className={styles.searchBtn}
              onClick={() => getBooks(search)}
            >
              Find books
            </button>
            <button className={styles.funBtn} onClick={getFunBooks}>
              Fun search
            </button>
          </div>
          <h3>Showing books on {randomWd.length ? randomWd : "Nothing"}</h3>
        </div>
        <div>
          {loading ? (
            <div className={styles.loader}>
              {/* {books.length ?  */}
              <ClipLoader color="#f4a261" />
              {/* // : null} */}
            </div>
          ) : (
            <BookCard books={books} />
          )}
        </div>
      </main>
    </>
  );
}
